#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	if (ac > 1)
	{
		while (av[i])
		{
			while (av[i][j])
				write (1, &av[i][j++], 1);
			i++;
			j = 0;
			write(1, "\n", 1);
		}
	}
	return (0);
}
